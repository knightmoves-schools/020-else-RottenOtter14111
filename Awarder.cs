namespace KnightMoves
{
    public class Awarder
    {
        public int points = 100;
       public string CalculateAward(points > 99){
           return "First Place";
       }
        else if(points > 49 && points < 100){
            return "Second Place";
        }
        else{
            return "Participation Award";
        };
    }
}
